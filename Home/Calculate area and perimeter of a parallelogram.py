# Calculate area and perimeter of a paralllelogram
l = float(input("Enter base/length of the parallelogram : "))
w = float(input("Enter width of the Parallelogram : "))
h = float(input("Enter height of the parallelogrm : "))
area = l*h
perimeter = 2*(l+w)
print("The area of given parallelogram is : ", area, "sq. units")
print("The perimeter of given parallelogram is : ", perimeter, "units")

